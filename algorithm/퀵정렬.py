# 입력 
# 첫 줄에 테스트케이스의 수 T가 주어진다. 1<=T<=50 
# 다음 줄부터 테스트 케이스의 별로 정수의 개수 N이 주어지고, 다음 줄에 N개의 정수 ai가 주어 진다. 
# 5<=N<=1,000,000, 0 <= ai <= 1,000,000 
 
# 출력 
# #과 1번부터인 테스트케이스 번호, 빈칸에 이어 오른쪽 원소가 먼저 복사되는 경우의 수, N//2번 원소를 출력한다